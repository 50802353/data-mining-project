    private void btnInputChooseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInputChooseActionPerformed
	if (chooserFileOpen == null) {
	    chooserFileOpen = new JFileChooser();

	}

	if (fileInput != null) {
	    chooserFileOpen.setCurrentDirectory(fileInput.getParentFile());
	}

	chooserFileOpen.showOpenDialog(this);
	File tmp = chooserFileOpen.getSelectedFile();

	if (tmp != null) {
	    fileInput = tmp;
	    txtInputFile.setText(fileInput.getAbsolutePath());
	}

    }//GEN-LAST:event_btnInputChooseActionPerformed

